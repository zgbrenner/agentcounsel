def run_check() -> int:
    if not SKILLS_DIR.is_dir():
        print("skills/ directory not found", file=sys.stderr)
        return 1
    areas = load_areas()
    expected = render_pack_registry(areas)
    if not PACKS_METADATA.is_file():
        print(f"MISSING: {PACKS_METADATA.relative_to(REPO_ROOT).as_posix()} "
              "has not been generated.")
        print("Run 'python scripts/build_platform_packs.py'.")
        return 1
    if PACKS_METADATA.read_text(encoding="utf-8") != expected:
        print(f"STALE: {PACKS_METADATA.relative_to(REPO_ROOT).as_posix()} "
              "is out of date.")
        print("Run 'python scripts/build_platform_packs.py'.")
        return 1
    problems = validate_pack_registry(build_pack_registry(areas))
    if problems:
        print("Pack manifest validation failed:")
        for problem in problems:
            print(f"  x {problem}")
        return 1
    print(f"{PACKS_METADATA.relative_to(REPO_ROOT).as_posix()} is up to date "
          f"({len(areas)} practice areas).")
    return 0


def run_write() -> int:
    if not SKILLS_DIR.is_dir():
        print("skills/ directory not found", file=sys.stderr)
        return 1

    commands = load_commands()
    areas = load_areas()
    if not areas:
        err("no practice areas with skills were found under skills/")

    PACKS_METADATA.parent.mkdir(parents=True, exist_ok=True)
    pack_registry = build_pack_registry(areas)
    PACKS_METADATA.write_text(
        json.dumps(pack_registry, indent=2) + "\n", encoding="utf-8")
    errors.extend(validate_pack_registry(pack_registry))

    shutil.rmtree(DIST, ignore_errors=True)
    DIST.mkdir(parents=True, exist_ok=True)
    write_text("packs.json", json.dumps(pack_registry, indent=2) + "\n")

    order = ordered_areas(areas)
    index_areas = []

    # ChatGPT: one consolidated file per area + a global instructions file.
    write_text("chatgpt/project-instructions.md",
               chatgpt_project_instructions(areas))
    for area in order:
        info = areas[area]
        text = chatgpt_pack(area, info, commands)
        check_safety(f"chatgpt/{area}.md", text)
        if len(text) < 1500:
            err(f"chatgpt/{area}.md: generated pack looks empty "
                f"({len(text)} bytes)")
        write_text(f"chatgpt/{area}.md", text)

    # Claude: one ZIP per area, folder structure preserved.
    for area in order:
        info = areas[area]
        nm = area_name(area)
        skills = info["skills"]
        members = [
            ("CLAUDE.md", claude_md(nm)),
            ("practice-profile-template.md", profile_text(area)),
            ("commands.md", commands_doc(nm, skills, commands)),
            ("README.md", claude_readme(nm, skills)),
        ]
        for sk in skills:
            members.append((f"skills/{sk['slug']}/SKILL.md", sk["raw"]))
            for tname, tcontent in sk["templates"]:
                members.append((f"templates/{sk['slug']}/{tname}", tcontent))
        for rname, rcontent in info["references"]:
            members.append((f"templates/references/{rname}", rcontent))
        if not any(a.startswith("templates/") for a, _ in members):
            members.append(("templates/README.md",
                            "The skills in this practice area do not ship "
                            "templates.\n"))
        check_safety(f"claude/{area}.zip:CLAUDE.md", members[0][1])
        check_not_empty(f"claude/{area}.zip", members)
        write_zip(f"claude/{area}.zip", members)

    # Gemini: one ZIP per area with four notebook sources.
    for area in order:
        info = areas[area]
        nm = area_name(area)
        members = [
            ("notebook-instructions.md", gemini_notebook_instructions(nm)),
            ("source-1-core-rules.md", gemini_core_source()),
            ("source-2-practice-profile.md",
             f"# AgentCounsel — {nm} Practice Profile (Source 2)\n\n"
             + GEN_NOTICE + "\n" + profile_text(area)),
            ("source-3-skills.md", gemini_skills_source(nm, info["skills"])),
            ("source-4-templates.md", gemini_templates_source(nm, info)),
        ]
        check_safety(f"gemini/{area}.zip:source-1-core-rules.md",
                     members[1][1])
        check_not_empty(f"gemini/{area}.zip", members)
        write_zip(f"gemini/{area}.zip", members)

    # Repo agents: instruction files for several agent environments.
    for fname, content in repo_agent_files().items():
        if len(content) < 200:
            err(f"repo-agents/{fname}: generated file looks empty")
        write_text(f"repo-agents/{fname}", content)

    # Index of practice areas and their packs.
    for area in order:
        info = areas[area]
        index_areas.append({
            "id": area,
            "name": area_name(area),
            "skill_count": len(info["skills"]),
            "skills": [{"slug": s["slug"], "name": s["name"],
                        "path": s["path"]} for s in info["skills"]],
            "packs": {
                "chatgpt": f"chatgpt/{area}.md",
                "claude": f"claude/{area}.zip",
                "gemini": f"gemini/{area}.zip",
            },
        })

    # Validation: every practice area must have an index entry.
    indexed = {a["id"] for a in index_areas}
    for area in areas:
        if area not in indexed:
            err(f"practice area '{area}' has no entry in dist/index.json")

    generated = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    total_skills = sum(len(i["skills"]) for i in areas.values())

    index = {
        "name": "AgentCounsel platform packs",
        "generated": generated,
        "generator": "scripts/build_platform_packs.py",
        "totals": {
            "practice_areas": len(index_areas),
            "skills": total_skills,
            "packs": len(index_areas) * 3 + 1 + 4,
        },
        "practice_areas": index_areas,
        "global": {
            "chatgpt_project_instructions": "chatgpt/project-instructions.md",
            "repo_agents": [
                "repo-agents/AGENTS.md", "repo-agents/CLAUDE.md",
                "repo-agents/.cursorrules", "repo-agents/README.md",
            ],
        },
    }
    write_text("index.json", json.dumps(index, indent=2) + "\n")
    write_text("SKILL_PACKS_INDEX.md", skill_packs_index(index_areas))

    # Manifest: every generated file with size and hash.
    files = []
    for path in sorted(written):
        data = path.read_bytes()
        files.append({
            "path": path.relative_to(DIST).as_posix(),
            "bytes": len(data),
            "sha256": hashlib.sha256(data).hexdigest(),
        })
    manifest = {
        "generated": generated,
        "generator": "scripts/build_platform_packs.py",
        "pack_registry": "metadata/packs.json",
        "file_count": len(files),
        "files": files,
    }
    (DIST / "manifest.json").write_text(
        json.dumps(manifest, indent=2) + "\n", encoding="utf-8")

    print(f"AgentCounsel platform packs -> {DIST}")
    print(f"  {len(index_areas)} practice areas, {total_skills} skills")
    print(f"  wrote {PACKS_METADATA.relative_to(REPO_ROOT).as_posix()}")
    print(f"  {len(files) + 1} files written "
          f"(chatgpt, claude, gemini, repo-agents, indexes)")

    errors.extend(validate_pack_registry(pack_registry, check_outputs=True))

    if errors:
        print()
        print(f"BUILD FAILED — {len(errors)} validation error(s):")
        for msg in errors:
            print(f"  x {msg}")
        return 1
    print("  all build-validation checks passed")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Build AgentCounsel platform packs and pack manifests.")
    parser.add_argument(
        "--check", action="store_true",
        help="Verify metadata/packs.json is current and valid; do not write "
             "dist/ or metadata files.")
    args = parser.parse_args()
    return run_check() if args.check else run_write()


