# --- writing ---------------------------------------------------------------

def write_text(rel: str, content: str) -> None:
    path = DIST / rel
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    written.append(path)


def write_zip(rel: str, members: list) -> None:
    path = DIST / rel
    path.parent.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(path, "w", zipfile.ZIP_DEFLATED) as zf:
        for arcname, content in members:
            info = zipfile.ZipInfo(arcname, date_time=ZIP_DATE)
            info.compress_type = zipfile.ZIP_DEFLATED
            info.external_attr = 0o644 << 16
            zf.writestr(info, content)
    written.append(path)


def check_safety(label: str, text: str) -> None:
    low = text.lower()
    for phrase in REQUIRED_SAFETY_PHRASES:
        if phrase.lower() not in low:
            err(f"{label}: required safety language missing ('{phrase}')")


def check_not_empty(label: str, members: list) -> None:
    total = sum(len(c) for _, c in members)
    if len(members) < 2 or total < 1000:
        err(f"{label}: generated pack looks empty "
            f"({len(members)} files, {total} bytes)")


