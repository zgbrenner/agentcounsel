def chatgpt_pack(area: str, info: dict, commands: dict) -> str:
    nm = area_name(area)
    skills = info["skills"]
    p = [f"# AgentCounsel — {nm} Pack (ChatGPT Projects)\n", GEN_NOTICE,
         f"This pack consolidates the AgentCounsel **{nm}** practice area for "
         f"a ChatGPT Project: platform instructions, the global safety rules, "
         f"the practice profile, the command list, every skill, the attorney "
         f"review checklist, and one-off usage examples — in a single file. "
         f"Every output produced with it is draft legal work product for "
         f"review by a licensed attorney; it is not legal advice.\n",
         "## 1. How to use this pack in a ChatGPT Project\n",
         chatgpt_instructions(nm),
         "## 2. Global safety rules\n",
         "These operating rules apply to every skill in this pack.\n"]
    for fname in CORE_SAFETY_FILES:
        p.append(demote((CORE_DIR / fname).read_text(encoding="utf-8"), 2))
    p.append("## 3. Practice profile\n")
    p.append("The practice profile records this team's jurisdictions, "
             "escalation thresholds, standard positions, and prohibited "
             "assumptions. Complete every placeholder before relying on it.\n")
    p.append(demote(profile_text(area), 2))
    p.append(f"## 4. Commands for {nm}\n")
    p.append("Slash-style shorthands for the skills in this pack.\n")
    p.append(commands_table(skills, commands))
    p.append("## 5. Skills\n")
    p.append(f"All {len(skills)} skills in the {nm} practice area. Each "
             f"produces draft legal work product for attorney review.\n")
    for sk in skills:
        p.append(skill_block(sk, 3))
    p.append("## 6. Attorney review checklist\n")
    p.append(demote((CORE_DIR / CORE_CHECKLIST_FILE).read_text(encoding="utf-8"),
                    2))
    p.append("## 7. One-off usage examples\n")
    p.append(usage_examples(area, skills))
    return "\n".join(p) + "\n"


