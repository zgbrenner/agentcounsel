def validate_pack_registry(registry: dict, check_outputs: bool = False) -> list[str]:
    problems: list[str] = []
    valid_checks = set(QUALITY_CHECK_FILES)
    for check_id, path in QUALITY_CHECK_FILES.items():
        if not (REPO_ROOT / path).exists():
            problems.append(f"quality check '{check_id}' target missing: {path}")
    for pack in registry.get("packs", []):
        pid = pack.get("pack_id", "<missing>")
        for field in (
            "pack_id", "platform", "practice_area", "included_skills",
            "included_core_rules", "included_quality_checks",
            "setup_instructions", "safety_disclaimer",
            "attorney_review_requirements", "version", "date",
        ):
            if field not in pack:
                problems.append(f"{pid}: missing manifest field '{field}'")
        for path in pack.get("included_skills", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: skill path missing: {path}")
        for path in pack.get("included_core_rules", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: core rule missing: {path}")
        for path in pack.get("included_templates", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: template/reference missing: {path}")
        for path in pack.get("included_matter_packs", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: matter pack missing: {path}")
        for path in pack.get("included_matter_workspace_templates", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: matter workspace missing: {path}")
        for path in pack.get("included_playbooks", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: playbook missing: {path}")
        for path in pack.get("included_review_panels", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: review panel missing: {path}")
        for path in pack.get("included_source_citation_templates", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: source/citation template missing: {path}")
        for path in pack.get("included_attorney_review_templates", []):
            if not (REPO_ROOT / path).is_file():
                problems.append(f"{pid}: attorney-review template missing: {path}")
        for check_id in pack.get("included_quality_checks", []):
            if check_id not in valid_checks:
                problems.append(f"{pid}: unknown quality check: {check_id}")
        if "draft legal work product" not in pack.get("safety_disclaimer", "").lower():
            problems.append(f"{pid}: safety disclaimer missing draft-work-product posture")
        if check_outputs:
            output = pack.get("output", "")
            if output.endswith("/"):
                if not (DIST / output).is_dir():
                    problems.append(f"{pid}: generated output folder missing: {output}")
            elif output and not (DIST / output).is_file():
                problems.append(f"{pid}: generated output file missing: {output}")
    return problems


def ordered_areas(areas: dict) -> list[str]:
    known = [a for a in AREA_ORDER if a in areas]
    return known + sorted(a for a in areas if a not in AREA_ORDER)


# --- markdown helpers ------------------------------------------------------

def demote(md: str, levels: int) -> str:
    if levels <= 0:
        return md
    out, fence = [], False
    for line in md.split("\n"):
        if line.lstrip().startswith("```"):
            fence = not fence
            out.append(line)
            continue
        if not fence:
            m = re.match(r"^(#{1,6})(\s)", line)
            if m:
                lvl = min(len(m.group(1)) + levels, 6)
                out.append("#" * lvl + line[len(m.group(1)):])
                continue
        out.append(line)
    return "\n".join(out)


def strip_first_h1(body: str) -> str:
    out, removed = [], False
    for line in body.split("\n"):
        if not removed and re.match(r"^#\s+\S", line):
            removed = True
            continue
        out.append(line)
    return "\n".join(out).strip()


def skill_block(sk: dict, base_level: int) -> str:
    """Render a skill under a heading of base_level, with its sections one
    level deeper."""
    head = "#" * base_level
    body = demote(strip_first_h1(sk["body"]), base_level - 1)
    return (f"{head} {sk['name']}\n\n"
            f"*Agent trigger:* {sk['description']}\n\n"
            f"*Canonical path:* `{sk['path']}`\n\n"
            f"{body}\n")


def profile_text(area: str) -> str:
    pf = PROFILES_DIR / f"{area}.md"
    if pf.is_file():
        return pf.read_text(encoding="utf-8")
    return (f"The {area_name(area)} practice area does not ship a standalone "
            f"practice profile. Configure agent behavior using the global "
            f"safety rules and your team's own conventions. See "
            f"`practice-profiles/README.md` in the AgentCounsel repository.\n")


def commands_table(skills: list, commands: dict) -> str:
    rows = ["| Command | Skill | Trigger phrases | Required inputs |"
            " Expected output |",
            "|---|---|---|---|---|"]
    for sk in skills:
        c = commands.get(sk["path"])
        if c:
            rows.append(f"| `{c['command']}` | {sk['name']} | {c['triggers']} "
                        f"| {c['inputs']} | {c['output']} |")
        else:
            rows.append(f"| _(no command)_ | {sk['name']} | "
                        f"{sk['description']} | | |")
    return "\n".join(rows) + "\n"


def usage_examples(area: str, skills: list) -> str:
    parts = ["These examples show one-off use — a single prompt pasted into "
             "any AI assistant, with no project setup. The skill text comes "
             "from the Skills section of this pack.\n"]
    for sk in skills[:2]:
        parts.append(f"**Using \"{sk['name']}\"**\n")
        parts.append(
            f"> Use the AgentCounsel \"{sk['name']}\" skill from this pack. "
            f"Follow its Workflow and Output Format exactly. Produce draft "
            f"legal work product for attorney review — this is not legal "
            f"advice. Do not invent legal authority, citations, quotations, "
            f"or deadlines; flag every gap with a placeholder such as "
            f"`[CONFIRM: ...]`. Then complete the skill's Attorney "
            f"Verification Checklist.\n>\n"
            f"> Paste the \"{sk['name']}\" skill section here, then provide "
            f"its Required Inputs. If an input is missing, stop and ask.\n")
    return "\n".join(parts)


