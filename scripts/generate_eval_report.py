#!/usr/bin/env python3
"""Generate deterministic AgentCounsel eval coverage report.

Standard-library only. This summarizes static eval coverage; it does not run
model outputs or claim substantive legal correctness.
"""

from __future__ import annotations

import argparse
import json
import sys
from collections import Counter, defaultdict
from pathlib import Path

import check_evals

REPO_ROOT = Path(__file__).resolve().parent.parent
REPORT_PATH = REPO_ROOT / "reports" / "eval-coverage.md"
EVAL_SUFFIX = ".eval.yaml"


def load_json(relpath: str) -> dict:
    return json.loads((REPO_ROOT / relpath).read_text(encoding="utf-8"))


def eval_file_cases(path: Path) -> tuple[str, list[dict]]:
    data = check_evals.parse_eval_yaml(path.read_text(encoding="utf-8"))
    if not isinstance(data, dict):
        return path.stem, []
    return str(data.get("skill") or data.get("suite") or path.stem), [
        c for c in (data.get("cases") or []) if isinstance(c, dict)
    ]


def count_skill_evals() -> dict[str, int]:
    out: dict[str, int] = {}
    for path in sorted((REPO_ROOT / "evals" / "skills").glob("*" + EVAL_SUFFIX)):
        data = check_evals.parse_eval_yaml(path.read_text(encoding="utf-8"))
        if not isinstance(data, dict):
            continue
        skill_path = data.get("skill_path")
        if isinstance(skill_path, str) and skill_path:
            parts = Path(skill_path).parts
            if len(parts) >= 3:
                out[f"{parts[-3]}/{parts[-2]}"] = len(data.get("cases") or [])
                continue
        slug, cases = eval_file_cases(path)
        out[slug] = len(cases)
    return out


def load_benchmark_cases() -> list[dict]:
    cases: list[dict] = []
    for folder in ("benchmarks", "router", "static"):
        base = REPO_ROOT / "evals" / folder
        if not base.is_dir():
            continue
        for path in sorted(base.glob("*" + EVAL_SUFFIX)):
            _, loaded = eval_file_cases(path)
            for case in loaded:
                case["_suite_type"] = folder
                cases.append(case)
    return cases


def render() -> str:
    index = load_json("metadata/index.json")
    packs = load_json("metadata/packs.json")
    skills = [s for s in index.get("skills", []) if isinstance(s, dict)]
    skill_eval_counts = count_skill_evals()
    benchmark_cases = load_benchmark_cases()

    by_area = Counter(s.get("practice_area") for s in skills)
    evals_by_area = Counter()
    evals_by_risk = Counter()
    quality_counter = Counter()
    for skill in skills:
        skill_id = skill.get("id")
        slug = str(skill.get("path", "")).split("/")[-2] if skill.get("path") else ""
        case_count = skill_eval_counts.get(skill_id, skill_eval_counts.get(slug, 0))
        if case_count:
            evals_by_area[skill.get("practice_area")] += case_count
            evals_by_risk[skill.get("risk_level")] += case_count
        for qid in skill.get("recommended_quality_checks") or []:
            if case_count:
                quality_counter[qid] += case_count

    benchmark_area = Counter(c.get("practice_area") for c in benchmark_cases if c.get("practice_area"))
    benchmark_quality = Counter()
    benchmark_status = Counter(c.get("eval_status") for c in benchmark_cases if c.get("eval_status"))
    for case in benchmark_cases:
        for qid in case.get("required_quality_checks") or case.get("expected_quality_checks") or []:
            benchmark_quality[qid] += 1

    missing_eval = []
    high_risk_low = []
    for skill in skills:
        skill_id = skill.get("id")
        slug = str(skill.get("path", "")).split("/")[-2] if skill.get("path") else ""
        count = skill_eval_counts.get(skill_id, skill_eval_counts.get(slug, 0))
        if count == 0:
            missing_eval.append(skill)
        if skill.get("risk_level") in {"high", "critical"} and count < 2:
            high_risk_low.append(skill)

    pack_counter = Counter()
    for pack in packs.get("packs", []):
        if not isinstance(pack, dict):
            continue
        pack_counter[pack.get("platform")] += 1

    lines = [
        "# Eval Coverage Report",
        "",
        "Generated by `scripts/generate_eval_report.py`. This report summarizes static and manual-ready eval coverage only. It does not claim legal correctness or model performance.",
        "",
        "## Summary",
        "",
        f"- Skills in metadata: {len(skills)}",
        f"- Skill eval files: {len(skill_eval_counts)}",
        f"- Skill eval cases: {sum(skill_eval_counts.values())}",
        f"- Benchmark/router/static cases: {len(benchmark_cases)}",
        f"- Skills without skill eval files: {len(missing_eval)}",
        f"- High or critical risk skills with fewer than two skill eval cases: {len(high_risk_low)}",
        "",
        "## Skill Eval Cases By Practice Area",
        "",
        "| Practice area | Skills | Skill eval cases | Benchmark cases |",
        "|---|---:|---:|---:|",
    ]
    for area in sorted(by_area):
        lines.append(
            f"| {area} | {by_area[area]} | {evals_by_area[area]} | {benchmark_area[area]} |")

    lines += [
        "",
        "## Skill Eval Cases By Risk Level",
        "",
        "| Risk level | Skill eval cases |",
        "|---|---:|",
    ]
    for risk in ("low", "medium", "high", "critical"):
        lines.append(f"| {risk} | {evals_by_risk[risk]} |")

    lines += [
        "",
        "## Eval Coverage By Quality Check",
        "",
        "| Quality check | Skill eval case references | Benchmark/router references |",
        "|---|---:|---:|",
    ]
    quality_ids = sorted({q.get("id") for q in index.get("quality_checks", []) if isinstance(q, dict)})
    for qid in quality_ids:
        lines.append(f"| {qid} | {quality_counter[qid]} | {benchmark_quality[qid]} |")

    lines += [
        "",
        "## Platform Pack Coverage",
        "",
        "| Platform | Pack count |",
        "|---|---:|",
    ]
    for platform in sorted(pack_counter):
        lines.append(f"| {platform} | {pack_counter[platform]} |")

    lines += [
        "",
        "## Benchmark Status",
        "",
        "| Status | Cases |",
        "|---|---:|",
    ]
    for status in sorted(benchmark_status):
        lines.append(f"| {status} | {benchmark_status[status]} |")

    lines += [
        "",
        "## Skills Without Skill Eval Coverage",
        "",
    ]
    if missing_eval:
        for skill in missing_eval:
            lines.append(f"- `{skill.get('id')}` at `{skill.get('path')}`")
    else:
        lines.append("- None.")

    lines += [
        "",
        "## High-Risk Coverage Gaps",
        "",
    ]
    if high_risk_low:
        for skill in high_risk_low:
            skill_id = skill.get("id")
            slug = str(skill.get("path", "")).split("/")[-2]
            lines.append(
                f"- `{skill.get('id')}` has {skill_eval_counts.get(skill_id, skill_eval_counts.get(slug, 0))} skill eval case(s).")
    else:
        lines.append("- None.")

    lines += [
        "",
        "## Known Gaps",
        "",
        "- Static evals validate structure, references, and candidate-output heuristics; they do not verify legal correctness.",
        "- Most skill eval cases are manual-ready and do not yet have candidate outputs.",
        "- Router evals define expected routing behavior but do not yet score live router responses.",
        "- Benchmark cases use fictional facts and are not copied from third-party legal benchmarks.",
        "",
    ]
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true", help="Fail if report is stale.")
    args = parser.parse_args()
    rendered = render()
    if args.check:
        if not REPORT_PATH.is_file():
            print(f"{REPORT_PATH.relative_to(REPO_ROOT).as_posix()} is missing")
            return 1
        if REPORT_PATH.read_text(encoding="utf-8") != rendered:
            print(f"{REPORT_PATH.relative_to(REPO_ROOT).as_posix()} is out of date")
            return 1
        print(f"{REPORT_PATH.relative_to(REPO_ROOT).as_posix()} is up to date.")
        return 0
    REPORT_PATH.write_text(rendered, encoding="utf-8")
    print(f"wrote {REPORT_PATH.relative_to(REPO_ROOT).as_posix()}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
